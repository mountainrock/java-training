package com.bri8.basic;

public class Variables {

	public static void main(String[] args) {
		int x = 5;     // Declaring and initializing an integer variable
		double pi;     // Declaring a double variable (without initialization)
		pi = 3.14;     // Assigning a value to the double variable

		char initial;  // Declaring a character variable (without initialization)
		initial = 'J'; // Assigning a value to the character variable

		String message; // Declaring a string variable (without initialization)
		message = "Hello, World!"; // Assigning a value to the string variable
		
		
	}
}
